        <ul>
          <li>Accedere alle informazioni personali raccolte da DmFixIt.</li>
          <li>Richiedere la correzione o la cancellazione delle informazioni personali.</li>
          <li>Opporsi al trattamento delle informazioni personali.</li>
        </ul>
