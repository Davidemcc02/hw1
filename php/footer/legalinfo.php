        <h2>Informativa sulla privacy</h2>
        <p>DmFixIt si impegna a proteggere la privacy degli Utenti che partecipano al Programma di riparazione. In questo documento, descriviamo come raccogliamo, utilizziamo e condividiamo le informazioni personali degli Utenti.</p>
        <h3>Raccolta delle informazioni</h3>
        <p>Quando l'Utente partecipa al Programma di riparazione, DmFixIt raccoglie le seguenti informazioni:</p>
        <ul>
          <li>Dati personali: nome, cognome, indirizzo, numero di telefono e indirizzo e-mail.</li>
          <li>Informazioni relative al dispositivo: tipo di dispositivo, modello, numero di serie e descrizione del problema.</li>
        </ul>
        <h3>Utilizzo delle informazioni</h3>
        <p>DmFixIt utilizza le informazioni raccolte per:</p>
        <ul>
          <li>Fornire il servizio di riparazione del dispositivo.</li>
          <li>Contattare l'Utente per informazioni relative alla riparazione del dispositivo.</li>
          <li>Migliorare la qualità del servizio di riparazione.</li>
        </ul>
        <h3>Condivisione delle informazioni</h3>
        <p>DmFixIt non condivide le informazioni personali degli Utenti con terze parti, ad eccezione dei casi in cui:</p>
        <ul>
          <li>è necessario per fornire il servizio di riparazione del dispositivo.</li>
          <li>è richiesto dalla legge o da un'ordinanza del tribunale.</li>
        </ul>
        <h3>Sicurezza delle informazioni</h3>
        <p>DmFixIt adotta misure di sicurezza appropriate per proteggere le informazioni personali degli Utenti, incluyendo:</p>
        <ul>
          <li>Crittografia dei dati.</li>
          <li>Accesso limitato alle informazioni.</li>
          <li>Misure di sicurezza fisiche per proteggere i dati.</li>
        </ul>
